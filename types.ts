//a type created for the result of the api
export interface GetCharacterResults {
  info: Info;//Info make reference from export interface Info
  results: Character[];//character make reference from export interface Character
}
//interface -> when you'll use types and not values
export interface Info {
  count: number;
  pages: number;
  next: string;
  prev: null;
}

export interface Character {
  id: number;
  name: string;
  status: Status;
  species: Species;
  type: string;
  gender: Gender;
  origin: Location;
  location: Location;
  image: string;
  episode: string[];
  url: string;
  created: Date;
}
